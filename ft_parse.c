#include "ft_printf.h"

int		ft_parse(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] == '%')
		{
			if (str[i + 1] == 'd' || str[i + 1] == 'i')
				return (1);
			if (str[i + 1] == 'c')
				return (2);
			if (str[i + 1] == 's')
				return (3);
		}
		i++;
	}
	return (0);
}
